    private Object[] AttToArray(int idx) {
        Object[] _data = new Object[data.numInstances()];
        for (int k = 0; k < data.numInstances(); k++) {
            if (data.instance(k).isMissing(idx)) {
                _data[k] = (Object) null;
            } else if (data.attribute(idx).isNominal()) {
                _data[k] = (Object) data.instance(k).stringValue(idx);
            } else if (data.attribute(idx).isNumeric()) {
                _data[k] = (Object) data.instance(k).value(idx);
            }
        }
        return _data;
    }

    private void showDataSelectedSegment(int idxSgmt) {
        dvp.setAttributeData(AttToArray(idxSgmt));
        _g.resetControls();
        _g.setEnableComponents(true);
        _g.plot.setGraficarFlag(true);

        int[] arr = {idxSgmt};
        _g.plot.setAttributestoPlot(arr);
        _g.plot.setLimits(_g.calcularlimites(arr));
        _g.plot.repaint();
    }

    private void initGraph() {
        _g.setInstances(data);
        _g.plot.setGraficarFlag(true);
        _g.plot.setM_graphLocalWindow(false);
        _g.plot.setFlagLimits(false);
        _g.plot.setFlagOutOfRange(false);
        int[] arr = {this.currentVarSelected};
        _g.plot.setAttributestoPlot(arr);
        _g.plot.setLimits(_g.calcularlimites(arr));
        dataGraphPanel.setLayout(new BorderLayout());
        dataGraphPanel.add(_g, BorderLayout.CENTER);
        _g.plot.repaint();
    }
