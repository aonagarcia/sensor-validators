    private void jButton2ActionPerformed(ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        //jButton1.setEnabled(false);
        started = true;
        //20-06-2012 m_localValidation = new localValidation(c.BD, jSlider1.getValue());
        m_localValidation = new localValidation(data, jSlider1.getValue());
        //m_localValidation.setDelta(m_localValidation.getWindowWidthSamples());
        m_localValidation.setDelta(1);
        _g.plot.setM_graphLocalWindow(true);
        
        for (m_position = 0; m_position < data.numInstances() - m_localValidation.getWindowWidthSamples(); m_position += m_localValidation.getDelta()) {
            m_localValidation.performStepByStep(this.currentVarSelected, m_position);
            this.medianaTextField.setText(Utils.doubleToString(m_localValidation.getMediana(), 3));
            this.desvStdTextField.setText(Utils.doubleToString(m_localValidation.getDesvStdM(), 3));
            _g.plot.setOutLiersFlags(data.localOutLiers(this.currentVarSelected));
            
        }
        _g.plot.setM_localValidationWindowIni(m_position);
        _g.plot.setM_localValidationWindowFin(m_position + m_localValidation.getWindowWidthSamples());
        _g.plot.repaint();
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton1ActionPerformed(ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if (started == false) {
            m_localValidation = new localValidation(data, jSlider1.getValue());
            //m_localValidation.setDelta(m_localValidation.getWindowWidthSamples());
            m_localValidation.setDelta(1);
            m_position = 0;
            double[] lims=m_localValidation.performStepByStep(this.currentVarSelected, m_position);
            this.medianaTextField.setText(Utils.doubleToString(m_localValidation.getMediana(), 3));
            this.desvStdTextField.setText(Utils.doubleToString(m_localValidation.getDesvStdM(), 3));
            _g.plot.setM_graphLocalWindow(true);
            _g.plot.setUserMaxRange(lims[0]);
            _g.plot.setUserMinRange(lims[1]);
            _g.plot.setM_localValidationWindowIni(m_position);
            _g.plot.setM_localValidationWindowFin(m_position + m_localValidation.getWindowWidthSamples());
            started = true;
        } else {
            m_position += m_localValidation.getDelta();
            double[] lims=m_localValidation.performStepByStep(this.currentVarSelected, m_position);
            this.medianaTextField.setText(Utils.doubleToString(m_localValidation.getMediana(), 3));
            this.desvStdTextField.setText(Utils.doubleToString(m_localValidation.getDesvStdM(), 3));
            _g.plot.setUserMaxRange(lims[0]);
            _g.plot.setUserMinRange(lims[1]);
            _g.plot.setM_localValidationWindowIni(m_position);
            _g.plot.setM_localValidationWindowFin(m_position + m_localValidation.getWindowWidthSamples());
        }
        _g.plot.setOutLiersFlags(data.localOutLiers(this.currentVarSelected));
        _g.plot.repaint();
    }//GEN-LAST:event_jButton1ActionPerformed

