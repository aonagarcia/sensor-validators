    public void initListeners() {
        ChangeListener listenerjSpinner1 = new ChangeListener() {
            public void stateChanged(ChangeEvent e) {
                Object value = jSpinner1.getValue();
                if (((Integer)value).intValue()>currentValueSpinner)
                {
                    Object [] fila = new Object[3];
                    fila[0]=((Integer)value).intValue();
                    modelo.addRow(fila);
                }
                else
                {
                    modelo.removeRow(currentValueSpinner-1);
                }                
                currentValueSpinner=((Integer)value).intValue();
            }
        };
        jSpinner1.addChangeListener (listenerjSpinner1);
    }

